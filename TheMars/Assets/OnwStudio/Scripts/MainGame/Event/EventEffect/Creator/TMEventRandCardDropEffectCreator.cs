using Onw.Attribute;
using System.Collections;
using System.Collections.Generic;
using UnityEngine;

namespace TM.Event.Effect.Creator
{
    public enum TMCardKindForWhere
    {
        [InspectorName("건설")] CONSTRUCTION,
        [InspectorName("효과")] EFFECT,
        [InspectorName("모두")] ALL
    }

    [System.Serializable, SerializeReferenceDropdownName("특정 카드 무작위 버림")]
    public class TMEventRandCardDropEffectCreator : ITMEventEffectCreator
    {
        [field: SerializeField, DisplayAs("카드 종류")] public TMCardKindForWhere Kind { get; private set; } = TMCardKindForWhere.ALL;
        [field: SerializeField, DisplayAs("버릴 카드 개수")] public int DropCount { get; private set; } = 0;

        public ITMEventEffect CreateEffect()
        {
            return ITMEventEffectCreator.EventEffectGenerator.CreateEffect<TMEventRandCardDropEffect, TMEventRandCardDropEffectCreator>(this);
        }
    }
}
